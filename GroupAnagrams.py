class Solution:

    # Time: O(m*nlogn)
    # Space: O(m*n)
    def groupAnagramsSorting(self, strs: List[str]) -> List[List[str]]:
        res = defaultdict(list)
        for s in strs:
            sortedS = ''.join(sorted(s))
            res[sortedS].append(s)
        return list(res.values())

    # Time: O(m*n)
    # Space: O(m*n)
    def groupAnagramsHash(self, strs: List[str]) -> List[List[str]]:
        res = defaultdict(list)
        for s in strs:
            count = [0] * 26
            for c in s:
                count[ord(c) - ord('a')] += 1
            res[tuple(count)].append(s)
        return list(res.values())